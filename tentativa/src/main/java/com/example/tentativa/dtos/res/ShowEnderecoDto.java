package com.example.tentativa.dtos.res;

// Classe que representa um DTO (Data Transfer Object) para mostrar informações de um endereço
public class ShowEnderecoDto {
    // Atributo que armazena o nome da rua
    private String rua;

    // Atributo que armazena o número da residência ou estabelecimento
    private String numero;

    // Atributo que armazena o nome da cidade
    private String cidade;

    // Método getter para obter o nome da rua
    public String getRua() {
        return rua;
    }

    // Método setter para definir o nome da rua
    public void setRua(String rua) {
        this.rua = rua;
    }

    // Método getter para obter o número
    public String getNumero() {
        return numero;
    }

    // Método setter para definir o número
    public void setNumero(String numero) {
        this.numero = numero;
    }

    // Método getter para obter o nome da cidade
    public String getCidade() {
        return cidade;
    }

    // Método setter para definir o nome da cidade
    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

//     Resumo dos
//     Componentes
// Classe ShowEnderecoDto:
//     Esta classe
//     é um

//     DTO (Data Transfer Object) que encapsula os dados que representam um endereço. É usado para transferir informações sobre o endereço entre a camada de serviço e a camada de apresentação.

// Atributos:

// rua: Um String que armazena o nome da rua.
// numero: Um String que armazena o número da residência ou estabelecimento.
// cidade: Um String que armazena o nome da cidade.
// Métodos Getter e Setter: Cada atributo possui métodos

//     para obter (getter)

//     e definir (setter) seus valores, permitindo que a classe mantenha o encapsulamento e o acesso controlado às suas propriedades.

// Se precisar de mais informações ou tiver mais perguntas, é só avisar!
}
